public class Sequencia2 {
    public static void main(String[] args) {

        for (int n = 1; n <= 10; n++) {
            
            System.out.print("\n" + n + ", ");

            for (int i = 1; i <= n; i++) {
                System.out.print(i + " ");
            }


        }
        
    }
}