package Aula10.InteiroImpar0a20;

public class InteiroImpar0a20 {
    public static void main(String[] args) {
        
        int contador = 0;

        while (contador <= 20) {
            if (contador % 2 == 1) {
                System.out.println(contador);
            }
            contador++;
        }

    }
}
