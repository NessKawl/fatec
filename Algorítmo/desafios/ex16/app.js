let n1 = parseFloat(prompt('Digite um número'))
let n2 = parseFloat(prompt('Digite outro número'))

let soma = n1 + n2;
let subtracao = n1 - n2;

console.log(`
    Os números digitados foram ${n1} e ${n2} \n
    A soma dos números ${n1} e ${n2} é ${soma} \n
    A subtração dos números ${n1} e ${n2} é ${subtracao} \n
`)